#include <stdio.h>
#include <stdlib.h>


// C data types


int main()
{
    int num1 = 10;          //Positive integer value

    int num2 = -1;          //Negative integer value

    float num3 = 3.5;       //Float value

    double num4 = 4.5;      //double value

    long num5 = 1023465890; //Long integer value

    char alphabet = 'A';    //character

    char name[] = "Nipuni"; //character string



    printf("num1: %d \n",num1);

    printf("num2: %d \n",num2);

    printf("num3: %f \n",num3);

    printf("num4: %lf \n",num4);

    printf("num5: %d \n",num5);

    printf("alphabet: %c \n",alphabet);

    printf("name: %s",name);

    return 0;
}
